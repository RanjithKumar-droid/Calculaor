package forgithub;

public class Calculations {
	
	void Add(double q,double r) {
		double ans = q+r;
		System.out.println("Ans: "+ans);
	}
	
	void Sub(double q, double r) {
		double ans = q-r;
		System.out.println("Ans: "+ans);
	}
	
	void Mul(double q, double r) {
		double ans = q*r;
		System.out.println("Ans: "+ans);
	}
	
}
